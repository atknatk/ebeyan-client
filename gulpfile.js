var gulp = require('gulp'),
    del = require('del')


/////////////////////////////////////////////////////////////////////////////////////
//
// cleans the build output
//
/////////////////////////////////////////////////////////////////////////////////////

gulp.task('clean', function (cb) {
    del([
        'dist'
    ], cb);
});


/////////////////////////////////////////////////////////////////////////////////////
//
// move builded files
//
/////////////////////////////////////////////////////////////////////////////////////

gulp.task('move', function(){
    gulp.src(['code/dist/**/*.*'], { base: './code/dist' })
        .pipe(gulp.dest('electron-builder/app'));
    gulp.src(['electron-builder/_app/**/*.*'], { base: './electron-builder/_app' })
        .pipe(gulp.dest('electron-builder/app'));
});


/////////////////////////////////////////////////////////////////////////////////////
//
// runs bower to install frontend dependencies
//
/////////////////////////////////////////////////////////////////////////////////////

gulp.task('default', ['move']);